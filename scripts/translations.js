/* Current language */
let currentLang = localStorage.getItem('kumbhsathi_lang') || 'hi';

/* Get a translation string */
function t(key) {
  return (T[currentLang] && T[currentLang][key]) || T['hi'][key] || T['en'][key] || key;
}

/* Set language and persist */
function setLang(lang) {
  if (!T[lang]) return;
  currentLang = lang;
  localStorage.setItem('kumbhsathi_lang', lang);
  // Update body class for font switching
  document.body.className = document.body.className
    .replace(/lang-\w+/g, '').trim();
  document.body.classList.add('lang-' + lang);
  // Re-render all translatable elements
  applyTranslations();
  // Update language button active states
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

/* Apply translations to all elements with data-t attribute */
function applyTranslations() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.dataset.t;
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-t-html]').forEach(el => {
    el.innerHTML = t(el.dataset.tHtml);
  });
  document.querySelectorAll('[data-t-title]').forEach(el => {
    el.title = t(el.dataset.tTitle);
  });
  document.querySelectorAll('[data-t-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.tPlaceholder);
  });
}

/* Initialize on load */
document.addEventListener('DOMContentLoaded', () => {
  document.body.classList.add('lang-' + currentLang);
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
});
